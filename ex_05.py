#Crie um programa que solicite um número inteiro e mostre o dobro, o triplo e a raiz quadrada desse número.

numero = int(input("Digite um número: "))
dobro = numero*2
triplo = numero*3
raiz  = int(numero**0.5)

print(f"O dobro de {numero} é {dobro}, o triplo é {triplo}, e a raiz quadrada é {raiz}.")


